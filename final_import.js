async function run() {
    console.log("🚀 Lancement de l'importation finale des clients...");

    const lines = rawData.split('\n').filter(l => l.trim() !== '');
    const data = lines.map(line => {
        const parts = line.split(';');
        if (parts.length < 3) return null;

        const [dateStr, name, amountStr] = parts;

        // Parse Date DD/MM/YYYY
        const [day, month, year] = dateStr.split('/');
        const date = new Date(`${year}-${month}-${day}T12:00:00Z`);

        // Parse Amount "4 914 700,00" -> 4914700.00
        const solde = parseFloat(amountStr.replace(/\s/g, '').replace(',', '.'));

        return {
            raison_sociale: name.trim(),
            contact: name.trim(),
            telephone: "Non renseigné",
            statut: 'actif',
            solde_initial: solde,
            created_at: date.toISOString(),
            temperature: 'chaud',
            secteur: 'AUTRE'
        };
    }).filter(x => x !== null);

    console.log(`📊 ${data.length} clients à importer.`);

    // 1. Suppression des anciens clients actifs et données liées via SQL rpc si possible,
    // Sinon on le fait manuellement ici
    console.log("🧹 Nettoyage des anciens clients actifs...");
    const { data: targets } = await supabase.from('prospects').select('id').eq('statut', 'actif');
    const targetIds = (targets || []).map(t => t.id);

    if (targetIds.length > 0) {
        // Ordre de suppression pour éviter les FK errors
        await supabase.from('missions').delete().in('prospect_id', targetIds);
        await supabase.from('paiements').delete().in('client_id', targetIds);
        await supabase.from('interventions').delete().in('client_id', targetIds);
        await supabase.from('prospect_history').delete().in('prospect_id', targetIds);
        await supabase.from('installations').delete().in('client_id', targetIds);
        await supabase.from('prospects').delete().in('id', targetIds);
    }

    // 2. Importation
    let successCount = 0;
    for (const item of data) {
        const { error } = await supabase.from('prospects').insert([item]);
        if (error) {
            console.error(`❌ Erreur ${item.raison_sociale}:`, error.message);
        } else {
            console.log(`✅ ${item.raison_sociale} importé.`);
            successCount++;
        }
    }

    console.log(`\n✨ Terminé. ${successCount}/${data.length} clients importés.`);
}

run();
